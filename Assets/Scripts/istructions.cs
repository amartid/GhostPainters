    }

    // Update is called once per frame

    void Update() 
    {   

        if(speackTarget){
            GameObject popupbox = GameObject.Find("Game Manager").gameObject;
            pop = popupbox.GetComponent<PopUpSystem>();
            if( Input.GetKeyDown("space"))
            {
                if(pop.popUpBox.GetComponent<Animator>().GetCurrentAnimatorStateInfo(0).IsName("pop"))
                {   
                    pop.popUpBox.GetComponent<Animator>().Play("Base Layer.close", 0, 0.25f);
                }
                else
                {
                    ShowSpeechTextPop();
                }
            }
        }
    }

    void ShowSpeechTextPop()
    {   
        GameObject popupbox = GameObject.Find("Game Manager").gameObject;
        pop = popupbox.GetComponent<PopUpSystem>();

        pop.popUpText.text = string.Join('\n',lines);
        pop.PopUp();
    }
