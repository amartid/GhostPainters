    void OnTriggerEnter2D(Collider2D other)
    {
        speackTarget = GameObject.FindGameObjectWithTag("Player").GetComponent<Transform>();
    }

    void OnTriggerExit2D(Collider2D other)
    {
        speackTarget = null;
    }
    
    // Start is called before the first frame update
